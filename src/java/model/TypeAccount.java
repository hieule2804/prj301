package model;

/**
 *
 * @author ADMIN
 */
public class TypeAccount {
    private int id;
    private String categories;

    public TypeAccount() {
    }

    public TypeAccount(int id, String categories) {
        this.id = id;
        this.categories = categories;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategories() {
        return categories;
    }

    public void setCategories(String categories) {
        this.categories = categories;
    }

    @Override
    public String toString() {
        return "TypeAccount{" + "id=" + id + ", categories=" + categories + '}';
    }
    
}
